package com.utkal.supply.model;

public class JarDefaulter {
	
	private int customerId;
	private  String customerName;
	private String mobileNumber;
	private String normalJar;
	private String coldJar;
	
	
	
	
	
	public int getCustomerId() {
		return customerId;
	}
	public void setCustomerId(int customerId) {
		this.customerId = customerId;
	}
	public String getCustomerName() {
		return customerName;
	}
	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}
	public String getMobileNumber() {
		return mobileNumber;
	}
	public void setMobileNumber(String mobileNumber) {
		this.mobileNumber = mobileNumber;
	}
	public String getNormalJar() {
		return normalJar;
	}
	public void setNormalJar(String normalJar) {
		this.normalJar = normalJar;
	}
	public String getColdJar() {
		return coldJar;
	}
	public void setColdJar(String coldJar) {
		this.coldJar = coldJar;
	}
	
	
	

}
